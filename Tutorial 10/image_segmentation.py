class GunDataset(Dataset):
    def __init__(self, root, split="train", img_size=128):
        self.img_size = img_size
        # ✅ images are directly in split folder, no /images/ subfolder
        self.img_dir  = os.path.join(root, split)
        ann_file      = os.path.join(root, split, "_annotations.coco.json")

        with open(ann_file) as f:
            coco = json.load(f)

        self.id2file = {img["id"]: img["file_name"] for img in coco["images"]}

        self.id2anns = {}
        for ann in coco["annotations"]:
            iid = ann["image_id"]
            self.id2anns.setdefault(iid, []).append(ann)

        self.image_ids = list(self.id2file.keys())
        print(f"  {split}: {len(self.image_ids)} images loaded")

    def __len__(self):
        return len(self.image_ids)

    def __getitem__(self, idx):
        iid      = self.image_ids[idx]
        img_path = os.path.join(self.img_dir, self.id2file[iid])

        img = cv2.imread(img_path)
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        h, w = img.shape[:2]

        # build mask from polygons
        mask = np.zeros((h, w), dtype=np.uint8)
        for ann in self.id2anns.get(iid, []):
            for seg in ann.get("segmentation", []):
                pts = np.array(seg).reshape(-1, 2).astype(np.int32)
                cv2.fillPoly(mask, [pts], 1)

        img  = cv2.resize(img,  (self.img_size, self.img_size))
        mask = cv2.resize(mask, (self.img_size, self.img_size),
                          interpolation=cv2.INTER_NEAREST)

        img  = torch.tensor(img.astype(np.float32) / 255.0).permute(2, 0, 1)
        mask = torch.tensor(mask.astype(np.float32)).unsqueeze(0)
        return img, mask

# ── Model ──────────────────────────────────────────────
class DoubleConv(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.block = nn.Sequential(
            nn.Conv2d(in_ch, out_ch, 3, padding=1, bias=False),
            nn.BatchNorm2d(out_ch), nn.ReLU(inplace=True),
            nn.Conv2d(out_ch, out_ch, 3, padding=1, bias=False),
            nn.BatchNorm2d(out_ch), nn.ReLU(inplace=True),
        )
    def forward(self, x): return self.block(x)

class EncoderBlock(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.conv = DoubleConv(in_ch, out_ch)
        self.pool = nn.MaxPool2d(2)
    def forward(self, x):
        skip = self.conv(x)
        return skip, self.pool(skip)

class DecoderBlock(nn.Module):
    def __init__(self, in_ch, out_ch):
        super().__init__()
        self.up   = nn.ConvTranspose2d(in_ch, out_ch, 2, 2)
        self.conv = DoubleConv(out_ch * 2, out_ch)
    def forward(self, x, skip):
        x = self.up(x)
        if x.shape != skip.shape:
            x = nn.functional.interpolate(x, size=skip.shape[2:])
        return self.conv(torch.cat([x, skip], dim=1))

class UNet(nn.Module):
    def __init__(self):
        super().__init__()
        self.enc1       = EncoderBlock(3, 64)
        self.enc2       = EncoderBlock(64, 128)
        self.bottleneck = DoubleConv(128, 256)
        self.dec1       = DecoderBlock(256, 128)
        self.dec2       = DecoderBlock(128, 64)
        self.out        = nn.Conv2d(64, 1, 1)
    def forward(self, x):
        s1, x = self.enc1(x)
        s2, x = self.enc2(x)
        x     = self.bottleneck(x)
        x     = self.dec1(x, s2)
        x     = self.dec2(x, s1)
        return torch.sigmoid(self.out(x))

# ── Train & Eval functions ──────────────────────────────
def train_epoch(model, loader, optimizer, criterion, device):
    model.train()
    total = 0
    for imgs, masks in loader:
        imgs, masks = imgs.to(device), masks.to(device)
        optimizer.zero_grad()
        loss = criterion(model(imgs), masks)
        loss.backward()
        optimizer.step()
        total += loss.item()
    return total / len(loader)

def eval_epoch(model, loader, criterion, device):
    model.eval()
    total = 0
    with torch.no_grad():
        for imgs, masks in loader:
            imgs, masks = imgs.to(device), masks.to(device)
            total += criterion(model(imgs), masks).item()
    return total / len(loader)

