class GunDataset(Dataset):
    def __init__(self, root, split="train", img_size=256):
        self.img_size = img_size
        self.split    = split                          # ✅ NEW — track split
        self.img_dir  = os.path.join(root, split)
        ann_file      = os.path.join(root, split, "_annotations.coco.json")

        with open(ann_file) as f:
            coco = json.load(f)

        self.id2file = {img["id"]: img["file_name"] for img in coco["images"]}

        self.id2anns = {}
        for ann in coco["annotations"]:
            iid = ann["image_id"]
            self.id2anns.setdefault(iid, []).append(ann)

        self.image_ids = list(self.id2file.keys())
        print(f"  {split}: {len(self.image_ids)} images loaded")

    # ✅ NEW — augmentation function
    def augment(self, img, mask):
        # Random horizontal flip
        if random.random() > 0.5:
            img  = cv2.flip(img,  1)
            mask = cv2.flip(mask, 1)

        # Random vertical flip
        if random.random() > 0.5:
            img  = cv2.flip(img,  0)
            mask = cv2.flip(mask, 0)

        # Random brightness
        if random.random() > 0.5:
            factor = random.uniform(0.7, 1.3)
            img    = np.clip(img * factor, 0, 255).astype(np.uint8)

        # Random rotation
        if random.random() > 0.5:
            angle = random.randint(-30, 30)
            h, w  = img.shape[:2]
            M     = cv2.getRotationMatrix2D((w//2, h//2), angle, 1.0)
            img   = cv2.warpAffine(img,  M, (w, h))
            mask  = cv2.warpAffine(mask, M, (w, h))

        # ✅ NEW — Random zoom in
        if random.random() > 0.5:
            scale  = random.uniform(0.8, 1.0)
            h, w   = img.shape[:2]
            nh, nw = int(h * scale), int(w * scale)
            y1     = (h - nh) // 2
            x1     = (w - nw) // 2
            img    = img[y1:y1+nh,  x1:x1+nw]
            mask   = mask[y1:y1+nh, x1:x1+nw]
            img    = cv2.resize(img,  (w, h))
            mask   = cv2.resize(mask, (w, h),
                                interpolation=cv2.INTER_NEAREST)

        return img, mask

    def __len__(self):
        return len(self.image_ids)

    def __getitem__(self, idx):
        iid      = self.image_ids[idx]
        img_path = os.path.join(self.img_dir, self.id2file[iid])

        img = cv2.imread(img_path)
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        h, w = img.shape[:2]

        mask = np.zeros((h, w), dtype=np.uint8)
        for ann in self.id2anns.get(iid, []):
            for seg in ann.get("segmentation", []):
                pts = np.array(seg).reshape(-1, 2).astype(np.int32)
                cv2.fillPoly(mask, [pts], 1)

        # ✅ NEW — only augment train, never validation
        if self.split == "train":
            img, mask = self.augment(img, mask)

        img  = cv2.resize(img,  (self.img_size, self.img_size))
        mask = cv2.resize(mask, (self.img_size, self.img_size),
                          interpolation=cv2.INTER_NEAREST)

        img  = torch.tensor(img.astype(np.float32) / 255.0).permute(2, 0, 1)
        mask = torch.tensor(mask.astype(np.float32)).unsqueeze(0)
        return img, mask

