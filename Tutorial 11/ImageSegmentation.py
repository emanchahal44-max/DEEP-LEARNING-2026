# ── Patch annotation filenames to use ONLY basenames (fixes path mismatch) ──
def patch_annotation_file(ann_file):
    """
    Roboflow sometimes stores file_name as 'train/images/foo.jpg'
    but the actual file is at img_dir/foo.jpg.
    This function rewrites all file_name entries to use only the basename.
    Returns path to patched file (or original if no change needed).
    """
    with open(ann_file) as f:
        data = json.load(f)

    changed = False
    for img_entry in data.get("images", []):
        original = img_entry["file_name"]
        cleaned  = os.path.basename(original)
        if cleaned != original:
            img_entry["file_name"] = cleaned
            changed = True

    if changed:
        patched_path = ann_file.replace(".json", "_patched.json")
        with open(patched_path, "w") as f:
            json.dump(data, f)
        print(f"   ✔ Patched annotation paths → {os.path.basename(patched_path)}")
        return patched_path
    else:
        print(f"   ✔ Annotation paths already clean — no patching needed")
        return ann_file


