def find_split_paths(root):
    """
    Recursively search `root` for train/valid/test splits.
    Returns dict: { 'train': (img_dir, ann_file), 'valid': ..., 'test': ... }

    Handles all Roboflow folder layouts:
      Layout A:  root/train/images/*.jpg  +  root/train/_annotations.coco.json
      Layout B:  root/train/*.jpg         +  root/train/_annotations.coco.json
      Layout C:  root/images/train/*.jpg  +  root/annotations/train.json
    """
    splits   = {}
    keywords = ["train", "valid", "test"]

    # Walk every subdirectory
    for dirpath, dirnames, filenames in os.walk(root):
        # Check if this folder name matches a split keyword
        folder_name = os.path.basename(dirpath).lower()

        for kw in keywords:
            if kw in folder_name and kw not in splits:

                # ── Find annotation JSON ───────────────────────────────────
                ann_file = None
                for fname in filenames:
                    if fname.endswith(".json"):
                        ann_file = os.path.join(dirpath, fname)
                        break

                # Also check one level up for the JSON
                if ann_file is None:
                    parent = os.path.dirname(dirpath)
                    for fname in os.listdir(parent):
                        if fname.endswith(".json") and kw in fname.lower():
                            ann_file = os.path.join(parent, fname)
                            break

                if ann_file is None:
                    continue  # No annotation file found for this split

                # ── Find image directory ───────────────────────────────────
                # Check for an "images" subfolder inside this split folder
                img_dir = None
                images_sub = os.path.join(dirpath, "images")
                if os.path.isdir(images_sub):
                    img_dir = images_sub
                else:
                    # Images are directly in this folder
                    img_exts = {".jpg", ".jpeg", ".png", ".bmp"}
                    has_imgs = any(
                        os.path.splitext(f)[1].lower() in img_exts
                        for f in filenames
                    )
                    if has_imgs:
                        img_dir = dirpath

                if img_dir is None:
                    continue

                # ── Verify annotation filenames match actual files ─────────
                # Read JSON, check if first image filename exists
                try:
                    with open(ann_file) as f:
                        ann_data = json.load(f)
                    if ann_data.get("images"):
                        sample_fname = ann_data["images"][0]["file_name"]
                        # Strip any subdirectory prefix Roboflow may have added
                        sample_fname = os.path.basename(sample_fname)
                        candidate    = os.path.join(img_dir, sample_fname)
                        if not os.path.exists(candidate):
                            # Try parent directory
                            candidate2 = os.path.join(dirpath, sample_fname)
                            if os.path.exists(candidate2):
                                img_dir = dirpath
                except Exception:
                    pass

                splits[kw] = (img_dir, ann_file)
                print(f"   [{kw}] images → {img_dir}")
                print(f"   [{kw}] labels → {ann_file}")

    return splits


