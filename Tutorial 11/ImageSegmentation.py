def train_one_epoch(model, optimizer, loader, device, epoch, num_epochs):
    model.train()
    total_loss = 0.0
    t0 = time.time()
    print(f"\n  {'─'*60}")
    print(f"  📌 EPOCH {epoch}/{num_epochs}  —  TRAINING")
    print(f"  {'─'*60}")

    for i, (images, targets) in enumerate(loader):
        images  = [img.to(device) for img in images]
        targets = [{k: v.to(device) for k, v in t.items()} for t in targets]

        loss_dict = model(images, targets)
        losses    = sum(loss for loss in loss_dict.values())

        optimizer.zero_grad()
        losses.backward()
        optimizer.step()
        total_loss += losses.item()

        if i % 5 == 0 or i == len(loader) - 1:
            print(f"    [{i+1:3d}/{len(loader)}] "
                  f"total={losses.item():.4f} | "
                  f"cls={loss_dict['loss_classifier'].item():.4f} | "
                  f"box={loss_dict['loss_box_reg'].item():.4f} | "
                  f"mask={loss_dict['loss_mask'].item():.4f} | "
                  f"rpn={loss_dict['loss_objectness'].item():.4f} "
                  f"({time.time()-t0:.1f}s)")

    avg = total_loss / len(loader)
    print(f"\n   Epoch {epoch} TRAIN avg loss: {avg:.5f}")
    return avg


def validate_one_epoch(model, loader, device, epoch):
    model.train()   # train mode needed to get loss dict
    total_loss = 0.0
    print(f"\n   EPOCH {epoch}  —  VALIDATION")

    with torch.no_grad():
        for images, targets in loader:
            images  = [img.to(device) for img in images]
            targets = [{k: v.to(device) for k, v in t.items()} for t in targets]
            loss_dict = model(images, targets)
            total_loss += sum(loss for loss in loss_dict.values()).item()

    avg = total_loss / len(loader)
    print(f"  Epoch {epoch} VALID avg loss: {avg:.5f}")
    return avg

