def visualize_ground_truth(img_dir, ann_file, num_samples=6, save_path=None):
    """
    Show sample images with ground-truth masks and bounding boxes.
    Uses only the basename of each filename to avoid path mismatch.
    """
    coco    = COCO(ann_file)
    img_ids = coco.getImgIds()
    img_ids = [i for i in img_ids if len(coco.getAnnIds(imgIds=i)) > 0]
    img_ids = img_ids[:num_samples]

    cols  = min(3, len(img_ids))
    rows  = (len(img_ids) + cols - 1) // cols
    fig, axes = plt.subplots(rows, cols, figsize=(6 * cols, 5 * rows))
    axes  = np.array(axes).flatten() if rows * cols > 1 else [axes]
    fig.patch.set_facecolor("#0f0f0f")
    fig.suptitle("Ground Truth Labels — Your Guns Dataset",
                 fontsize=15, fontweight="bold", color="white", y=1.01)

    for ax, img_id in zip(axes, img_ids):
        info = coco.loadImgs(img_id)[0]

        # ── KEY FIX: always use basename ──────────────────────────────────
        fname    = os.path.basename(info["file_name"])
        img_path = os.path.join(img_dir, fname)

        if not os.path.exists(img_path):
            ax.set_title(f"Missing: {fname}", color="red", fontsize=8)
            ax.axis("off")
            continue

        image   = np.array(Image.open(img_path).convert("RGB"))
        ann_ids = coco.getAnnIds(imgIds=img_id)
        anns    = coco.loadAnns(ann_ids)

        ax.imshow(image)
        ax.set_facecolor("#0f0f0f")
        np.random.seed(img_id)

        for ann in anns:
            color = np.random.rand(3)

            if ann.get("segmentation"):
                mask = coco.annToMask(ann)
                rgba = np.zeros((*mask.shape, 4))
                rgba[mask == 1] = [*color, 0.45]
                ax.imshow(rgba)

                contours, _ = cv2.findContours(
                    mask.astype(np.uint8),
                    cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE
                )
                for cnt in contours:
                    pts = cnt.squeeze()
                    if pts.ndim == 2:
                        ax.plot(pts[:, 0], pts[:, 1],
                                color=color, linewidth=1.5)

            x, y, w, h = ann["bbox"]
            ax.add_patch(plt.Rectangle(
                (x, y), w, h, linewidth=2,
                edgecolor=color, facecolor="none"
            ))
            cat_name = coco.loadCats(ann["category_id"])[0]["name"]
            ax.text(x, y - 4, cat_name, color="white", fontsize=8,
                    fontweight="bold",
                    bbox=dict(facecolor=color, alpha=0.7,
                              pad=2, edgecolor="none"))

        ax.set_title(f"ID {img_id} — {len(anns)} object(s)",
                     fontsize=10, color="white")
        ax.axis("off")

    for ax in axes[len(img_ids):]:
        ax.set_visible(False)

    plt.tight_layout()
    if save_path:
        plt.savefig(save_path, dpi=150, bbox_inches="tight",
                    facecolor=fig.get_facecolor())
        print(f"   Saved → {save_path}")
    plt.show()


