def run_inference(model, image_input, device, conf_threshold=0.5):
    if isinstance(image_input, str):
        image_pil = Image.open(image_input).convert("RGB")
    else:
        image_pil = image_input

    img_t = TF.to_tensor(image_pil).to(device)
    with torch.no_grad():
        pred = model([img_t])[0]

    keep = pred["scores"] > conf_threshold
    return {
        "image":  image_pil,
        "boxes":  pred["boxes"][keep].cpu().numpy(),
        "labels": pred["labels"][keep].cpu().numpy(),
        "scores": pred["scores"][keep].cpu().numpy(),
        "masks":  pred["masks"][keep].cpu().numpy(),
    }


def draw_predictions(result, class_names, mask_threshold=0.5,
                     title="Predictions", save_path=None):
    image_np = np.array(result["image"]).copy()
    overlay  = image_np.copy()
    n        = len(result["boxes"])

    np.random.seed(99)
    legend_patches = []

    for i in range(n):
        color        = tuple(np.random.randint(80, 240, 3).tolist())
        x1,y1,x2,y2 = result["boxes"][i].astype(int)
        cls_id       = result["labels"][i]
        score        = result["scores"][i]
        mask         = result["masks"][i, 0] > mask_threshold
        cls_name     = (class_names[cls_id]
                        if cls_id < len(class_names) else f"cls_{cls_id}")

        # Fill mask
        overlay[mask] = (overlay[mask] * 0.4 +
                         np.array(color, dtype=np.uint8) * 0.6).astype(np.uint8)

        # Mask outline
        contours, _ = cv2.findContours(mask.astype(np.uint8),
                                       cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        cv2.drawContours(image_np, contours, -1, color, 2)

        # Box + label
        cv2.rectangle(image_np, (x1, y1), (x2, y2), color, 2)
        label_text  = f"{cls_name}: {score:.2f}"
        (tw, th), _ = cv2.getTextSize(label_text, cv2.FONT_HERSHEY_SIMPLEX, 0.55, 2)
        cv2.rectangle(image_np, (x1, y1-th-10), (x1+tw+4, y1), color, -1)
        cv2.putText(image_np, label_text, (x1+2, y1-4),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.55, (255,255,255), 2)

        legend_patches.append(mpatches.Patch(
            color=[c/255 for c in color],
            label=f"#{i+1} {cls_name} ({score:.2f})"
        ))

    final = cv2.addWeighted(overlay, 0.55, image_np, 0.45, 0)

    fig = plt.figure(figsize=(14, 6), facecolor="#0d0d0d")
    gs  = gridspec.GridSpec(1, 2, figure=fig, wspace=0.04)

    ax1 = fig.add_subplot(gs[0])
    ax1.imshow(result["image"]); ax1.axis("off")
    ax1.set_title("Original", color="white", fontsize=12)
    ax1.set_facecolor("#0d0d0d")

    ax2 = fig.add_subplot(gs[1])
    ax2.imshow(final); ax2.axis("off")
    ax2.set_title(f"{n} Detection(s)", color="white", fontsize=12)
    ax2.set_facecolor("#0d0d0d")
    if legend_patches:
        ax2.legend(handles=legend_patches, loc="upper right",
                   fontsize=9, framealpha=0.85,
                   facecolor="#1a1a1a", labelcolor="white")

    fig.suptitle(title, color="white", fontsize=13, fontweight="bold", y=1.01)
    plt.tight_layout()
    if save_path:
        plt.savefig(save_path, dpi=150, bbox_inches="tight",
                    facecolor=fig.get_facecolor())
    plt.show()
    return final


