class GunSegmentationDataset(Dataset):

    def __init__(self, img_dir, ann_file, augment=False):
        self.img_dir  = img_dir
        self.augment  = augment
        self.coco     = COCO(ann_file)

        all_ids      = self.coco.getImgIds()
        self.img_ids = [i for i in all_ids
                        if len(self.coco.getAnnIds(imgIds=i)) > 0]

        cats = self.coco.loadCats(self.coco.getCatIds())
        # Map COCO category id → model label (1-based; 0 = background)
        self.cat_to_label = {cat["id"]: idx + 1 for idx, cat in enumerate(cats)}
        self.class_names  = ["background"] + [cat["name"] for cat in cats]

    def __len__(self):
        return len(self.img_ids)

    def __getitem__(self, idx):
        img_id   = self.img_ids[idx]
        img_info = self.coco.loadImgs(img_id)[0]

        # ── KEY FIX: always resolve to basename ───────────────────────────
        fname    = os.path.basename(img_info["file_name"])
        img_path = os.path.join(self.img_dir, fname)

        image    = Image.open(img_path).convert("RGB")
        W, H     = image.size

        ann_ids = self.coco.getAnnIds(imgIds=img_id)
        anns    = self.coco.loadAnns(ann_ids)

        boxes, labels, masks = [], [], []

        for ann in anns:
            x, y, w, h     = ann["bbox"]
            x1,y1,x2,y2    = x, y, x+w, y+h
            if x2 <= x1 or y2 <= y1:
                continue
            boxes.append([x1, y1, x2, y2])
            labels.append(self.cat_to_label[ann["category_id"]])
            masks.append(self.coco.annToMask(ann))

        if len(boxes) == 0:
            boxes   = torch.zeros((0, 4),    dtype=torch.float32)
            labels  = torch.zeros((0,),      dtype=torch.int64)
            masks   = torch.zeros((0, H, W), dtype=torch.uint8)
            area    = torch.zeros((0,),      dtype=torch.float32)
            iscrowd = torch.zeros((0,),      dtype=torch.uint8)
        else:
            boxes   = torch.as_tensor(boxes,           dtype=torch.float32)
            labels  = torch.as_tensor(labels,          dtype=torch.int64)
            masks   = torch.as_tensor(np.stack(masks), dtype=torch.uint8)
            area    = (boxes[:,3]-boxes[:,1]) * (boxes[:,2]-boxes[:,0])
            iscrowd = torch.zeros((len(boxes),),        dtype=torch.uint8)

        target = {
            "boxes":    boxes,
            "labels":   labels,
            "masks":    masks,
            "image_id": torch.tensor([img_id]),
            "area":     area,
            "iscrowd":  iscrowd,
        }

        img_tensor = TF.to_tensor(image)

        # Horizontal flip augmentation (training only)
        if self.augment and torch.rand(1).item() > 0.5:
            img_tensor = TF.hflip(img_tensor)
            _, H_t, W_t = img_tensor.shape
            if len(target["boxes"]) > 0:
                fb = target["boxes"].clone()
                fb[:, 0] = W_t - target["boxes"][:, 2]
                fb[:, 2] = W_t - target["boxes"][:, 0]
                target["boxes"] = fb
            if len(target["masks"]) > 0:
                target["masks"] = torch.flip(target["masks"], dims=[2])

        return img_tensor, target


