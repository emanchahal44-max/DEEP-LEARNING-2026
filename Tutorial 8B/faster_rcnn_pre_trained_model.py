# AP calculation
def compute_ap(pred_boxes, pred_scores, gt_boxes, iou_thr=0.5):
    if len(pred_boxes) == 0 or len(gt_boxes) == 0:
        return 0.0
    order   = np.argsort(-np.array(pred_scores))
    matched = set()
    tp_list, fp_list = [], []
    for idx in order:
        best_iou, best_j = 0, -1
        for j, gt in enumerate(gt_boxes):
            if j in matched: continue
            iou = calculate_iou(pred_boxes[idx], gt)
            if iou > best_iou:
                best_iou, best_j = iou, j
        if best_iou >= iou_thr and best_j != -1:
            tp_list.append(1); fp_list.append(0); matched.add(best_j)
        else:
            tp_list.append(0); fp_list.append(1)

    tp_cum     = np.cumsum(tp_list)
    fp_cum     = np.cumsum(fp_list)
    recalls    = tp_cum / max(len(gt_boxes), 1)
    precisions = tp_cum / (tp_cum + fp_cum + 1e-6)
    recalls    = np.concatenate([[0], recalls,    [1]])
    precisions = np.concatenate([[0], precisions, [0]])
    for i in range(len(precisions)-2, -1, -1):
        precisions[i] = max(precisions[i], precisions[i+1])
    return np.sum((recalls[1:]-recalls[:-1]) * precisions[1:])

