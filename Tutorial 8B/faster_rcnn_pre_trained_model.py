    boxes  = preds[0]['boxes'].numpy()
    scores = preds[0]['scores'].numpy()
    labels = preds[0]['labels'].numpy()
    keep   = scores >= threshold
    return image, boxes[keep], scores[keep], labels[keep], elapsed

