# YOLO STEP 2 — DETECTION HEAD
# Divides image into 13×13 grid
# Each cell predicts 3 boxes
# Each box = 4 coords + 1 confidence + 20 class probs = 25
# Total output = 13×13×3×25
# ────────────────────────────────────────────────────────────────
print("\n── YOLO Step 2: Building Detection Head...")

class YOLOHead(nn.Module):
    def __init__(self, in_channels=512, num_classes=20, num_boxes=3):
        super(YOLOHead, self).__init__()
        self.num_classes = num_classes
        self.num_boxes   = num_boxes

        # 5 = 4 coords + 1 confidence
        num_outputs = num_boxes * (num_classes + 5)

        self.detection = nn.Conv2d(
            in_channels, num_outputs,
            kernel_size=1, padding=0
        )

    def forward(self, x):
        batch    = x.size(0)
        out      = self.detection(x)    # (B, num_boxes*(20+5), 13, 13)

        # Reshape to (B, 13, 13, num_boxes, 20+5)
        out = out.permute(0, 2, 3, 1).contiguous()
        out = out.view(batch, 13, 13, self.num_boxes, self.num_classes + 5)

        return out

