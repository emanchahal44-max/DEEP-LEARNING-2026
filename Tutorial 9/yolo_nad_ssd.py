def detect_ssd(img_tensor, threshold=0.3):
    ssd_model.eval()
    with torch.no_grad():
        out = ssd_model([img_tensor.to(device)])[0]
    boxes  = out['boxes'].cpu().numpy()
    labels = out['labels'].cpu().numpy()
    scores = out['scores'].cpu().numpy()
    mask   = scores >= threshold
    return boxes[mask], labels[mask], scores[mask]

def draw_boxes(img_tensor, boxes, labels, scores, color=(0,220,0)):
    img = (img_tensor.permute(1,2,0).numpy()*255).astype(np.uint8)
    img = cv2.cvtColor(img, cv2.COLOR_RGB2BGR)
    for box, label, score in zip(boxes, labels, scores):
        x1,y1,x2,y2 = map(int, box)
        name = CLASS_NAMES[label] if label < len(CLASS_NAMES) else f"obj_{label}"
        text = f"{name}: {score:.2f}"
        cv2.rectangle(img, (x1,y1), (x2,y2), color, 3)
        (tw,th),_ = cv2.getTextSize(text, cv2.FONT_HERSHEY_SIMPLEX, 0.6, 2)
        cv2.rectangle(img, (x1,y1-th-8), (x1+tw+4,y1), color, -1)
        cv2.putText(img, text, (x1+2,y1-4),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.6, (0,0,0), 2)
    return cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

