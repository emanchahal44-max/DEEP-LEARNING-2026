class GunsDataset(Dataset):
    def __init__(self, img_dir, ann_file):
        self.img_dir = img_dir
        self.coco    = COCO(ann_file)
        self.img_ids = list(self.coco.imgs.keys())
        self.tf      = T.Compose([T.ToTensor()])
        print(f"  Loaded {len(self.img_ids)} images")

    def __len__(self):
        return len(self.img_ids)

    def __getitem__(self, idx):
        img_id   = self.img_ids[idx]
        img_info = self.coco.imgs[img_id]
        image    = Image.open(os.path.join(self.img_dir,
                    img_info["file_name"])).convert("RGB")
        anns     = self.coco.loadAnns(self.coco.getAnnIds(imgIds=img_id))

        boxes, labels = [], []
        for ann in anns:
            x, y, w, h = ann["bbox"]
            if w > 1 and h > 1:
                boxes.append([x, y, x+w, y+h])
                labels.append(1)

        if len(boxes) == 0:
            boxes  = torch.zeros((0,4), dtype=torch.float32)
            labels = torch.zeros((0,),  dtype=torch.int64)
        else:
            boxes  = torch.tensor(boxes,  dtype=torch.float32)
            labels = torch.tensor(labels, dtype=torch.int64)

        target = {
            "boxes"   : boxes,
            "labels"  : labels,
            "image_id": torch.tensor([img_id]),
            "area"    : (boxes[:,3]-boxes[:,1])*(boxes[:,2]-boxes[:,0]) if len(boxes) else torch.zeros(0),
            "iscrowd" : torch.zeros(len(labels), dtype=torch.int64)
        }
        return self.tf(image), target

def collate_fn(batch):
    return tuple(zip(*batch))

