def coco_to_yolo(json_path, img_dir, out_img_dir, out_lbl_dir):
    with open(json_path) as f:
        data = json.load(f)
    img_lookup = {img['id']: img for img in data['images']}
    ann_by_img = {}
    for ann in data['annotations']:
        ann_by_img.setdefault(ann['image_id'], []).append(ann)

    import shutil
    for img_id, img_info in img_lookup.items():
        fname    = img_info['file_name']
        src_path = os.path.join(img_dir, fname)
        dst_path = os.path.join(out_img_dir, fname)
        if os.path.exists(src_path):
            shutil.copy(src_path, dst_path)

        W, H     = img_info['width'], img_info['height']
        anns     = ann_by_img.get(img_id, [])
        lbl_path = os.path.join(out_lbl_dir,
                    fname.replace('.jpg','.txt').replace('.png','.txt'))
        with open(lbl_path, 'w') as f:
            for ann in anns:
                x, y, w, h = ann['bbox']
                cx = (x + w/2) / W
                cy = (y + h/2) / H
                nw = w / W
                nh = h / H
                f.write(f"0 {cx:.6f} {cy:.6f} {nw:.6f} {nh:.6f}\n")

