for epoch in range(1, SSD_EPOCHS+1):
    ssd_model.train()
    total, count = 0.0, 0
    t0 = time.time()

    for i, (images, targets) in enumerate(train_loader):
        images  = [img.to(device) for img in images]
        targets = [{k: v.to(device) for k,v in t.items()} for t in targets]

        loss_dict = ssd_model(images, targets)
        losses    = sum(loss_dict.values())

        # ── FIX: Skip NaN batches ──────────────────────────
        if torch.isnan(losses):
            continue

        ssd_optim.zero_grad()
        losses.backward()
        # ── FIX: Gradient clipping stops exploding gradients
        torch.nn.utils.clip_grad_norm_(ssd_model.parameters(), max_norm=1.0)
        ssd_optim.step()

        total += losses.item()
        count += 1

        if (i+1) % 10 == 0:
            print(f"  Ep {epoch}/{SSD_EPOCHS} | Batch {i+1}/{len(train_loader)} "
                  f"| Loss: {losses.item():.4f}")

    ssd_sched.step()

    if count == 0:
        print(f"\n── Epoch {epoch} | All batches NaN — skipping\n")
        continue

    avg = total / count
    ssd_losses.append(avg)
    marker = ""
    if avg < ssd_best:
        ssd_best = avg
        torch.save(ssd_model.state_dict(), SSD_SAVE)
        marker = "  SAVED"

    print(f"\n── Epoch {epoch} | Avg Loss: {avg:.4f} | "
          f"Time: {time.time()-t0:.0f}s{marker}\n")

