# ────────────────────────────────────────────────────────────────
# SSD STEP 2 — PREDICTION HEAD
# Two heads attached to each feature map:
#   Classification head → what is the object?
#   Bounding box head   → where is the object? (x, y, w, h)
# ────────────────────────────────────────────────────────────────
print("\n── SSD Step 2: Building Prediction Head...")

class SSDHead(nn.Module):
    def __init__(self, in_channels, num_classes, num_boxes):
        super(SSDHead, self).__init__()
        self.num_classes = num_classes
        self.num_boxes   = num_boxes

        # Classification head — predicts class scores
        self.cls_head = nn.Conv2d(
            in_channels, num_boxes * num_classes,
            kernel_size=3, padding=1
        )

        # Bounding box head — predicts (x, y, w, h)
        self.box_head = nn.Conv2d(
            in_channels, num_boxes * 4,
            kernel_size=3, padding=1
        )

    def forward(self, x):
        batch = x.size(0)

        # Class predictions
        cls = self.cls_head(x)                          # (B, num_boxes*num_classes, H, W)
        cls = cls.permute(0, 2, 3, 1).contiguous()     # (B, H, W, num_boxes*num_classes)
        cls = cls.view(batch, -1, self.num_classes)     # (B, H*W*num_boxes, num_classes)

        # Box predictions
        box = self.box_head(x)                          # (B, num_boxes*4, H, W)
        box = box.permute(0, 2, 3, 1).contiguous()     # (B, H, W, num_boxes*4)
        box = box.view(batch, -1, 4)                    # (B, H*W*num_boxes, 4)

        return cls, box

print("  SSD Prediction Head defined!")
print("     Classification head → predicts object class")
print("     Bounding box head   → predicts (x, y, w, h)")


# ────────────────────────────────────────────────────────────────
# SSD STEP 3 — FULL SSD MODEL WITH MULTI-SCALE FEATURE MAPS
# Detects objects at 3 different scales:
#   Scale 1 → large feature map  → finds small objects
#   Scale 2 → medium feature map → finds medium objects
#   Scale 3 → small feature map  → finds large objects
# ────────────────────────────────────────────────────────────────
print("\n── SSD Step 3: Building Full SSD with Multi-Scale Feature Maps...")

class SSDModel(nn.Module):
    def __init__(self, num_classes=21, num_boxes=6):
        super(SSDModel, self).__init__()
        self.num_classes = num_classes
        self.num_boxes   = num_boxes

        # Backbone
        self.backbone = SSDBackbone()

        # Downsample layers for multi-scale (stride=2 halves the map)
        self.scale2 = nn.Conv2d(128, 128, kernel_size=3, stride=2, padding=1)
        self.scale3 = nn.Conv2d(128, 128, kernel_size=3, stride=2, padding=1)

        # One prediction head per scale
        self.head1 = SSDHead(128, num_classes, num_boxes)  # scale 1
        self.head2 = SSDHead(128, num_classes, num_boxes)  # scale 2
        self.head3 = SSDHead(128, num_classes, num_boxes)  # scale 3

    def forward(self, x):
        # Backbone features
        f1 = self.backbone(x)           # Scale 1 — large  (75×75)
        f2 = torch.relu(self.scale2(f1))  # Scale 2 — medium (38×38)
        f3 = torch.relu(self.scale3(f2))  # Scale 3 — small  (19×19)

        # Predictions at each scale
        cls1, box1 = self.head1(f1)
        cls2, box2 = self.head2(f2)
        cls3, box3 = self.head3(f3)

        # Concatenate all scale predictions
        all_cls = torch.cat([cls1, cls2, cls3], dim=1)  # (B, total_anchors, num_classes)
        all_box = torch.cat([box1, box2, box3], dim=1)  # (B, total_anchors, 4)

        return all_cls, all_box

