class SSDLoss(nn.Module):
    def __init__(self):
        super().__init__()
        self.cls_loss = nn.CrossEntropyLoss()
        self.box_loss = nn.SmoothL1Loss()

    def forward(self, pred_cls, pred_box, targets):
        batch_cls_loss = 0.0
        batch_box_loss = 0.0
        count = 0
        for i in range(len(targets)):
            gt_boxes  = targets[i]["boxes"].to(pred_cls.device)
            gt_labels = targets[i]["labels"].to(pred_cls.device)
            if len(gt_boxes) == 0:
                continue
            num_gt = min(len(gt_boxes), pred_cls.shape[1])
            pred_c = pred_cls[i, :num_gt, :]
            pred_b = pred_box[i, :num_gt, :]
            cls_t = gt_labels[:num_gt]
            box_t = gt_boxes[:num_gt]
            batch_cls_loss += self.cls_loss(pred_c, cls_t)
            batch_box_loss += self.box_loss(pred_b, box_t)
            count += 1
        if count == 0:
            return torch.tensor(0.0, requires_grad=True)
        return (batch_cls_loss + batch_box_loss) / count

