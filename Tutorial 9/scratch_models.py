class YOLOBackbone(nn.Module):
    def __init__(self):
        super().__init__()
        def block(cin, cout):
            return nn.Sequential(
                nn.Conv2d(cin, cout, 3, padding=1),
                nn.BatchNorm2d(cout),
                nn.LeakyReLU(0.1),
                nn.MaxPool2d(2,2)
            )
        self.b1 = block(3,   32)
        self.b2 = block(32,  64)
        self.b3 = block(64,  128)
        self.b4 = block(128, 256)
        self.b5 = block(256, 512)

    def forward(self, x):
        x = self.b1(x); x = self.b2(x)
        x = self.b3(x); x = self.b4(x)
        x = self.b5(x)
        return x

class YOLOHead(nn.Module):
    def __init__(self, in_ch=512, num_classes=1, num_boxes=3):
        super().__init__()
        self.num_classes = num_classes
        self.num_boxes   = num_boxes
        self.detect = nn.Conv2d(in_ch, num_boxes*(5+num_classes), 1)

    def forward(self, x):
        b   = x.size(0)
        out = self.detect(x)
        out = out.permute(0,2,3,1).contiguous()
        out = out.view(b, 13, 13, self.num_boxes, 5+self.num_classes)
        return out

class YOLOScratch(nn.Module):
    def __init__(self, num_classes=1, num_boxes=3):
        super().__init__()
        self.backbone = YOLOBackbone()
        self.head     = YOLOHead(512, num_classes, num_boxes)

    def forward(self, x):
        features = self.backbone(x)
        return self.head(features)

class YOLOLoss(nn.Module):
    def __init__(self):
        super().__init__()
        self.bce = nn.BCEWithLogitsLoss()
        self.mse = nn.MSELoss()

    def forward(self, pred, targets):
        B = pred.size(0)
        obj_loss  = 0.0
        box_loss  = 0.0
        count     = 0
        obj_pred  = pred[..., 4]
        no_obj_target = torch.zeros_like(obj_pred)
        obj_loss += self.bce(obj_pred, no_obj_target) * 0.5
        for b in range(B):
            gt_boxes = targets[b]["boxes"].to(pred.device)
            if len(gt_boxes) == 0:
                continue
            for gt_box in gt_boxes:
                cx = ((gt_box[0] + gt_box[2]) / 2 * 13).long().clamp(0, 12)
                cy = ((gt_box[1] + gt_box[3]) / 2 * 13).long().clamp(0, 12)
                obj_loss += self.bce(
                    pred[b, cy, cx, 0, 4].unsqueeze(0),
                    torch.ones(1).to(pred.device)
                )
                pred_box = torch.sigmoid(pred[b, cy, cx, 0, :4])
                box_loss += self.mse(pred_box,
                    torch.stack([
                        (gt_box[0]+gt_box[2])/2,
                        (gt_box[1]+gt_box[3])/2,
                        gt_box[2]-gt_box[0],
                        gt_box[3]-gt_box[1]
                    ]).to(pred.device))
                count += 1
        total = obj_loss + (box_loss / max(count,1))
        return total

