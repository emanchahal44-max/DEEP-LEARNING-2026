class SSDBackbone(nn.Module):
    def __init__(self):
        super().__init__()
        self.block1 = nn.Sequential(nn.Conv2d(3,32,3,padding=1),nn.BatchNorm2d(32),nn.ReLU(),nn.MaxPool2d(2,2))
        self.block2 = nn.Sequential(nn.Conv2d(32,64,3,padding=1),nn.BatchNorm2d(64),nn.ReLU(),nn.MaxPool2d(2,2))
        self.block3 = nn.Sequential(nn.Conv2d(64,128,3,padding=1),nn.BatchNorm2d(128),nn.ReLU())
    def forward(self, x):
        return self.block3(self.block2(self.block1(x)))

class SSDHead(nn.Module):
    def __init__(self, in_ch, num_classes, num_boxes):
        super().__init__()
        self.num_classes = num_classes
        self.num_boxes   = num_boxes
        self.cls = nn.Conv2d(in_ch, num_boxes*num_classes, 3, padding=1)
        self.box = nn.Conv2d(in_ch, num_boxes*4, 3, padding=1)
    def forward(self, x):
        b = x.size(0)
        cls = self.cls(x).permute(0,2,3,1).contiguous().view(b,-1,self.num_classes)
        box = self.box(x).permute(0,2,3,1).contiguous().view(b,-1,4)
        return cls, box

class SSDScratch(nn.Module):
    def __init__(self, num_classes=2, num_boxes=4):
        super().__init__()
        self.backbone = SSDBackbone()
        self.down1 = nn.Sequential(nn.Conv2d(128,128,3,stride=2,padding=1),nn.ReLU())
        self.down2 = nn.Sequential(nn.Conv2d(128,128,3,stride=2,padding=1),nn.ReLU())
        self.head1 = SSDHead(128, num_classes, num_boxes)
        self.head2 = SSDHead(128, num_classes, num_boxes)
        self.head3 = SSDHead(128, num_classes, num_boxes)
    def forward(self, x):
        f1 = self.backbone(x)
        f2 = self.down1(f1)
        f3 = self.down2(f2)
        c1,b1 = self.head1(f1)
        c2,b2 = self.head2(f2)
        c3,b3 = self.head3(f3)
        return torch.cat([c1,c2,c3],dim=1), torch.cat([b1,b2,b3],dim=1)

# ── Rebuild YOLO model ─────────────────────────────────────
class YOLOBackbone(nn.Module):
    def __init__(self):
        super().__init__()
        def block(cin, cout):
            return nn.Sequential(nn.Conv2d(cin,cout,3,padding=1),nn.BatchNorm2d(cout),nn.LeakyReLU(0.1),nn.MaxPool2d(2,2))
        self.b1=block(3,32); self.b2=block(32,64); self.b3=block(64,128)
        self.b4=block(128,256); self.b5=block(256,512)
    def forward(self, x):
        return self.b5(self.b4(self.b3(self.b2(self.b1(x)))))

class YOLOHead(nn.Module):
    def __init__(self, in_ch=512, num_classes=1, num_boxes=3):
        super().__init__()
        self.num_classes = num_classes
        self.num_boxes   = num_boxes
        self.detect = nn.Conv2d(in_ch, num_boxes*(5+num_classes), 1)
    def forward(self, x):
        b = x.size(0)
        out = self.detect(x).permute(0,2,3,1).contiguous()
        return out.view(b, 13, 13, self.num_boxes, 5+self.num_classes)

class YOLOScratch(nn.Module):
    def __init__(self, num_classes=1, num_boxes=3):
        super().__init__()
        self.backbone = YOLOBackbone()
        self.head     = YOLOHead(512, num_classes, num_boxes)
    def forward(self, x):
        return self.head(self.backbone(x))

