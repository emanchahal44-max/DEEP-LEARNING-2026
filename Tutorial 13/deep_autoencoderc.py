# ── Add Noise ───────────────────────────────────
def add_noise(images, noise_factor=0.5):
    """Add Gaussian noise to a batch of images."""
    noisy_images = images + noise_factor * torch.randn_like(images)
    noisy_images = torch.clamp(noisy_images, 0., 1.)
    return noisy_images

# ── Build Denoising Autoencoder ─────────────────
class DenoisingAutoencoder(nn.Module):
    def __init__(self):
        super().__init__()

        # Encoder
        self.encoder = nn.Sequential(
            nn.Conv2d(1, 32, kernel_size=3, padding=1),   # 32x28x28
            nn.ReLU(),
            nn.MaxPool2d(2, 2),                            # 32x14x14
            nn.Conv2d(32, 64, kernel_size=3, padding=1),  # 64x14x14
            nn.ReLU(),
            nn.MaxPool2d(2, 2)                             # 64x7x7
        )

        # Decoder
        self.decoder = nn.Sequential(
            nn.Conv2d(64, 64, kernel_size=3, padding=1),  # 64x7x7
            nn.ReLU(),
            nn.Upsample(scale_factor=2),                   # 64x14x14
            nn.Conv2d(64, 32, kernel_size=3, padding=1),  # 32x14x14
            nn.ReLU(),
            nn.Upsample(scale_factor=2),                   # 32x28x28
            nn.Conv2d(32, 1,  kernel_size=3, padding=1),  # 1x28x28
            nn.Sigmoid()
        )

    def forward(self, x):
        return self.decoder(self.encoder(x))

