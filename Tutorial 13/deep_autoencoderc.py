import torch
import torch.nn as nn
import torch.optim as optim
import torch.nn.functional as F
from torch.utils.data import DataLoader
from torchvision import datasets, transforms
import matplotlib.pyplot as plt

# ── Device ──────────────────────────────────────
device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
print(f"Using device: {device}")

# ── Load Dataset ────────────────────────────────
transform     = transforms.ToTensor()
train_dataset = datasets.MNIST(root='./data', train=True,  transform=transform, download=True)
test_dataset  = datasets.MNIST(root='./data', train=False, transform=transform, download=True)
train_loader  = DataLoader(train_dataset, batch_size=128, shuffle=True)
test_loader   = DataLoader(test_dataset,  batch_size=128, shuffle=False)

# ── VAE Architecture ─────────────────────────────
class VAE(nn.Module):
    def __init__(self, latent_dim=16):
        super().__init__()
        self.latent_dim = latent_dim

        # ── Encoder ──────────────────────────────
        # Input: 1x28x28
        self.enc_conv = nn.Sequential(
            nn.Conv2d(1, 32, kernel_size=3, padding=1),   # 32x28x28
            nn.ReLU(),
            nn.MaxPool2d(2, 2),                            # 32x14x14
            nn.Conv2d(32, 64, kernel_size=3, padding=1),  # 64x14x14
            nn.ReLU(),
            nn.MaxPool2d(2, 2),                            # 64x7x7
            nn.Conv2d(64, 128, kernel_size=3, padding=1), # 128x7x7
            nn.ReLU(),
        )
        self.flatten_dim = 128 * 7 * 7  # = 6272

        # Project to mean and log-variance of the latent distribution
        self.fc_mu     = nn.Linear(self.flatten_dim, latent_dim)
        self.fc_logvar = nn.Linear(self.flatten_dim, latent_dim)

        # ── Decoder ──────────────────────────────
        self.fc_decode = nn.Linear(latent_dim, self.flatten_dim)

        self.dec_conv = nn.Sequential(
            nn.Conv2d(128, 64, kernel_size=3, padding=1), # 64x7x7
            nn.ReLU(),
            nn.Upsample(scale_factor=2),                   # 64x14x14
            nn.Conv2d(64, 32, kernel_size=3, padding=1),  # 32x14x14
            nn.ReLU(),
            nn.Upsample(scale_factor=2),                   # 32x28x28
            nn.Conv2d(32, 1,  kernel_size=3, padding=1),  # 1x28x28
            nn.Sigmoid()
        )

    def encode(self, x):
        """Encode input image to latent parameters (mu, logvar)."""
        h = self.enc_conv(x)
        h = h.view(h.size(0), -1)          # Flatten
        mu     = self.fc_mu(h)
        logvar = self.fc_logvar(h)
        return mu, logvar

    def reparameterize(self, mu, logvar):
        """
        Reparameterization trick:
        z = mu + eps * std,  where eps ~ N(0, I)
        This allows gradients to flow through the sampling step.
        """
        if self.training:
            std = torch.exp(0.5 * logvar)   # std deviation
            eps = torch.randn_like(std)      # random noise
            return mu + eps * std
        else:
            return mu  # Use mean directly at inference

    def decode(self, z):
        """Decode latent vector back to image space."""
        h = self.fc_decode(z)
        h = h.view(h.size(0), 128, 7, 7)  # Reshape to feature map
        return self.dec_conv(h)

    def forward(self, x):
        mu, logvar = self.encode(x)
        z          = self.reparameterize(mu, logvar)
        recon      = self.decode(z)
        return recon, mu, logvar


# ── VAE Loss Function ────────────────────────────
def vae_loss(recon_x, x, mu, logvar):
    """
    VAE loss = Reconstruction loss + KL Divergence

    - Reconstruction loss (BCE): measures how well the decoder
      reconstructs the original image from the latent vector.
    - KL Divergence: regularizes the latent space to be close
      to a standard normal distribution N(0, I).
      KLD = -0.5 * sum(1 + logvar - mu^2 - exp(logvar))
    """
    bce = F.binary_cross_entropy(recon_x, x, reduction='sum')
    kld = -0.5 * torch.sum(1 + logvar - mu.pow(2) - logvar.exp())
    return bce + kld


