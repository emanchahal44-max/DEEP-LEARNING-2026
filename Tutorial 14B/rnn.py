# ────────────────────────────────────────────────────────────────
# SECTION 4 — MODEL DEFINITION
#
#  Input → Embedding → GRU → Dropout → Linear → Next-char logits
#
#  Why GRU instead of SimpleRNN?
#    GRU has gating (reset + update gates) so it remembers longer
#    patterns without vanishing gradients.
#
#  Dropout (0.4) + L2 weight_decay prevent the model from
#  memorising only the training names (the "Aaaaaa" repeating bug).
# ────────────────────────────────────────────────────────────────
class NameRNN(nn.Module):
    def __init__(self, vocab_size, embed_dim=16, hidden_dim=128):
        super().__init__()
        # Extra embedding slot for the learnable START token
        self.START_IDX = vocab_size
        self.embedding = nn.Embedding(vocab_size + 1, embed_dim)
        self.rnn       = nn.GRU(embed_dim, hidden_dim, batch_first=True)
        self.drop      = nn.Dropout(0.4)
        self.fc        = nn.Linear(hidden_dim, vocab_size)

    def forward(self, bx):
        """Training mode: full prefix sequence -> next char logit."""
        emb    = self.embedding(bx)           # (B, T, embed_dim)
        out, _ = self.rnn(emb)               # (B, T, hidden_dim)
        last   = self.drop(out[:, -1, :])    # take last time-step
        return self.fc(last)                  # (B, vocab_size)

    def forward_single(self, prefix_indices):
        """Generation mode: predict next char from current prefix."""
        tokens = [self.START_IDX] + list(prefix_indices)
        x      = torch.tensor([tokens], dtype=torch.long,
                               device=next(self.parameters()).device)
        emb    = self.embedding(x)
        out, _ = self.rnn(emb)
        last   = self.drop(out[:, -1, :])
        return self.fc(last)

