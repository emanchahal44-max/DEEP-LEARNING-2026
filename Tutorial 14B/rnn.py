# ────────────────────────────────────────────────────────────────
# SECTION 3 — BUILD TRAINING SAMPLES
#
#  For each name every (prefix → next_char) pair is one sample.
#  Example for "sara":
#    []          -> s
#    [s]         -> a
#    [s, a]      -> r
#    [s, a, r]   -> a
#    [s, a, r, a]-> <eos>
#
#  ~240 names × ~6 chars ≈ 1500 training pairs
# ────────────────────────────────────────────────────────────────
def make_samples(names):
    xs, ys = [], []
    for name in names:
        encoded = [ch2idx[c] for c in name.lower() if c in ch2idx]
        targets = encoded + [EOS_IDX]
        for i, target in enumerate(targets):
            xs.append(encoded[:i])   # prefix (may be empty)
            ys.append(target)        # next character to predict
    return xs, ys

